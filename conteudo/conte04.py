# Para importar bibliotecas e funcionalidades em Python utiliza-se o comando Import seguido do nome biblioteca ou módulo que ira ser carregado nesse programa. 
# Para importar coisas específicas de uma biblioteca utiliza-se o comando from seguido do nome da biblioteca seguido da palavra import seguido do item que deseja importar.

import math
num = int (input('Digite um número'))
raiz = math.sqrt(num)
print('A raiz de {} é igual a {}' .format(num, raiz))